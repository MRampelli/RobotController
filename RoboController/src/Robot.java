/**
 * Data class to store the properties of the Robot
 */
public class Robot {

	/**
	 * Robot current X position.
	 */
	int x;

	/**
	 * Robot current Y position.
	 */
	int y;

	/**
	 * Robot current direction
	 */
	char direction;

	/**
	 * String to store robot instructions/movements
	 */
	String instructions;
}
